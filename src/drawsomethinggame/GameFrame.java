    @Override
    public void update(Observable o, Object arg) {
        Scanner in = new Scanner((String) arg);
        String tag = in.nextLine();

        if (tag.equals("text")) {
            jTextArea1.append(in.nextLine() + "\n");
            if (jTextArea1.getLineCount() > 5) {
                jTextArea1.selectAll();
            }
        }
    }

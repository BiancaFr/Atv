    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        myName = jTextField1.getText();
        serverIP = jTextField2.getText();
        if (myPort == 0) {
            Random r = new Random();
            myPort = 3334 + r.nextInt(100);
        }
        ((Canvas) jPanel1).connect(myName, myPort, serverIP, serverPort);
        ((Canvas) jPanel1).addObserver(this);
        ((Canvas) jPanel1).sendMessage("text", myName + ": Entrou no jogo.");
    }//GEN-LAST:event_jButton1ActionPerformed

